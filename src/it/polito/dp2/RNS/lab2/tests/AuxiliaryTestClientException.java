package it.polito.dp2.RNS.lab2.tests;

public class AuxiliaryTestClientException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1371021528110110983L;

	public AuxiliaryTestClientException() {
	}

	public AuxiliaryTestClientException(String message) {
		super(message);
	}

	public AuxiliaryTestClientException(Throwable cause) {
		super(cause);
	}

	public AuxiliaryTestClientException(String message, Throwable cause) {
		super(message, cause);
	}

	public AuxiliaryTestClientException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
